def test_subtitle_generates_valid_ass(tmp_path: Path):
    """generate_ass_subtitles must produce a parseable .ass file with both styles."""
    from app.renderer.subtitle import generate_ass_subtitles
    from app.renderer.timeline import fixture_timeline
    from app.models.job import VisualDirection

    timeline = fixture_timeline(
        job_id="test-qa", keyword="test", output_path="/tmp/out.mp4"
    )

    # Inject emphasis on scene 0 to exercise viral_emphasis style
    vd = VisualDirection(subtitle_emphasis=True)
    object.__setattr__(timeline.scenes[0].props, 'visual_direction', vd)

    out = tmp_path / "subs.ass"
    generate_ass_subtitles(timeline, out)

    content = out.read_text(encoding="utf-8")
    assert "[V4+ Styles]" in content
    assert "viral_bold" in content
    assert "viral_emphasis" in content
    assert "[Events]" in content
    # Should have 5 Dialogue lines (one per scene)
    assert content.count("Dialogue:") == 5


