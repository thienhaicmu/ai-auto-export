# 5. Optional Playwright frame smoke test (skipped if Playwright unavailable)
# ═══════════════════════════════════════════════════════════════════════════════

def _playwright_available() -> bool:
    try:
        import playwright.sync_api  # noqa: F401
        return True
    except ImportError:
        return False


@pytest.mark.skipif(not _playwright_available(), reason="Playwright not installed")
def test_viral_template_first_frame_is_not_blank(tmp_path: Path):
    """
    Render frame 0 of the viral template fixture and verify it is:
      - a valid PNG file
      - has non-zero dimensions (1080×1920 for final mode)
      - has non-trivial pixel variance (not a blank/black frame)

    This is an optional end-to-end smoke test. Skipped if Playwright is
    not installed in the current environment.
    """
    from playwright.sync_api import sync_playwright
    from app.renderer.timeline import fixture_timeline

    timeline = fixture_timeline(
        job_id="smoke-test", keyword="karen", output_path=str(tmp_path / "out.mp4")
    )
    scene = timeline.scenes[0]
    scene_duration = scene.end - scene.start
    fps = timeline.fps

    html_path = _VIRAL_DIR / "index.html"
    assert html_path.exists(), "viral/index.html must exist"

    with sync_playwright() as p:
        browser = p.chromium.launch()
        context = browser.new_context(
            viewport={"width": timeline.resolution[0], "height": timeline.resolution[1]}
        )
        page = context.new_page()

        # Inject minimal scene props
        scene_payload = {
            "index": scene.index,
            "start": scene.start,
            "end":   scene.end,
            "template": "viral",
            "role": "hook",
            "props": {
                "headline": scene.props.headline,
                "subhead":  scene.props.subhead,
                "animation_seed": scene.props.animation_seed,
                "highlight_word_indices": scene.props.highlight_word_indices,
            },
            "audio": {},
        }
        page.add_init_script(f"window.__SCENE__ = {scene_payload!r};")

        page.goto(html_path.as_uri())

        # Wait for SCENE_READY
        page.wait_for_function("window.__SCENE_READY__ === true", timeout=10_000)

        # Step to frame 0 (t=0)
        page.evaluate("""
            document.getAnimations().forEach(function(a) {
                a.currentTime = 0;
                a.pause();
            });
        """)

        frame_path = tmp_path / "frame_0000.png"
        page.screenshot(path=str(frame_path), full_page=False)

        browser.close()

    assert frame_path.exists(), "Screenshot must be saved"
    assert frame_path.stat().st_size > 5_000, \
        f"Frame PNG is suspiciously small ({frame_path.stat().st_size} bytes) — may be blank"
